% Exemplo de declaração de fatos usando átomos e números (constantes)

% Valores em 10/03/2026
cotacao(dolar, 5.16).
cotacao(euro, 6.01).
cotacao(iene, 0.03).

% O primeiro argumento é nome, o segundo a idade
paciente('Carlos', 38).
paciente('Maria', 26).
paciente('Pedro Lucas', 40).

% Átomos podem conter underscore e números
limites(eixo_x, -10, 10).
limites(eixo_y, -1, 5).
limites(area1, -1, 2).
limites(area2, 0, 3).
